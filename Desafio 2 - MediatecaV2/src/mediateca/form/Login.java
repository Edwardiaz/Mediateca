    public void validarAcceso (){
        int resultado=0;
        String carnet = jTextField1.getText();
        
        if(!carnet.equals("")){
        try{
            
            String sql = "select * from socios where documento='"+carnet+"'";
            PreparedStatement stmt = con.prepareStatement(sql);
            ResultSet rs=stmt.executeQuery(sql);
            
             
            if(rs.next()){
                resultado=1;
                
                if(resultado == 1){
                    
                    JOptionPane.showMessageDialog(null, "Bienvenido: " + carnet);
                    Dashboard db = new Dashboard();
                    db.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Error en los datos introducidos");
                }
            }else{
                    JOptionPane.showMessageDialog(null, "Error en los datos introducidos"); 
                }
        }catch(Exception e) {
                    JOptionPane.showMessageDialog(null, "Error en el acceso al sistema" + e.getMessage());
            }
        }else {
             JOptionPane.showMessageDialog(null, "No ha introducido nada. Intente nuevamente"); 
        }    
    }
    

